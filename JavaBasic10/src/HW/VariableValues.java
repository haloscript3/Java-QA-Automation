package HW;

public class VariableValues {
    public static void main(String[] args) {


        byte age = 51;
        short num1 = 1;
        int salary = 60000;
        long ID = 2106248196412812875L;
        float shiftStarts = 1.30F;
        double shiftEnds = 11.30;
        char favoriteCharacter = '$';
        boolean isWorking = false;

        System.out.println("Age = " + age);
        System.out.println("num = " + num1);
        System.out.println("Salary = " + salary);
        System.out.println("Identification = " + ID);
        System.out.println("Shift Starts at = " + shiftStarts);
        System.out.println("Shift Ends at = " + shiftEnds);
        System.out.println("Favorite character = " + favoriteCharacter);
        System.out.println("Is Working = " + isWorking + "\n\n");

        age = 55;
        num1 = 3;//??????
        salary = 115000;
        ID = 2106248196412812875L;
        shiftStarts = 3.30F;
        shiftEnds = 9.00D;
        favoriteCharacter = '@';
        isWorking = true;

        System.out.println("Age = " + age);
        System.out.println("num = " + num1);
        System.out.println("Salary = " + salary);
        System.out.println("Identification = " + ID);
        System.out.println("Shift Starts at = " + shiftStarts);
        System.out.println("Shift Ends at = " + shiftEnds);
        System.out.println("Favorite character = " + favoriteCharacter);
        System.out.println("Is Working = " + isWorking);
    }
}

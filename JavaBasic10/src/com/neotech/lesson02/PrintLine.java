package com.neotech.lesson02;

public class PrintLine {
    public static void main(String[] args) {
        //println prints whole statements and ads newline
        System.out.println("Whats up?");
        System.out.println("How are you doing?");

        //print prints whole statements and doesn't add newline
        System.out.print("I am doing good!");
        System.out.print("1");
        System.out.print("2");
        System.out.print("3");
        System.out.print("4");

        System.out.println(); // newLine

        //\n == newline
        System.out.println("How are you \nI am good!");

        System.out.print("\n");
        System.out.print("\n");
        System.out.print("\n");
        System.out.print("\n");
        System.out.print("\n");
        System.out.println("Hello there!");

        System.out.print("\n");
        System.out.print("\n");

        System.out.println("\n\n");
        System.out.println();
        System.out.println("\n\n");
        System.out.println("Bye now \\n");

    }
}
